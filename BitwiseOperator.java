public class BitwiseOperator{
    public static void main(String args[]){
        int a = 10;
        int b =  2;
        int c = a&b; //Bitwise AND
        System.out.println(c);
        int d = a|b; //Bitwise OR
        System.out.println(d);
        int e = a^b; //Ex-OR
        System.out.println(e);
    }
}