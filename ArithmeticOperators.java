class ArithmeticOperators{
    public static void main(String args[]){
        int a = 5;
        int b = 2;
        int Addition = a + b; // addition 7
        int Subtraction = a - b; // subtraction 3
        int Multiplication = a * b; // multiplication 10
        int Division = a / b; // division 2
        int Modules = a % b; // modules 1
        System.out.println("Arithmetic Operator in Java");
        System.out.println("Addition of a and b is "+Addition);
        System.out.println("Subtraction of a and b is "+Subtraction);
        System.out.println("Multiplication of a and b is "+Multiplication);
        System.out.println("Division of a and b is "+Division);
        System.out.println("Modules of a and b is "+Modules);
    }
}